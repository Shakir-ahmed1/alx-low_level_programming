#include "3-calc.h"
/**
 * op_add - it adds two numbers
 * @a: first number
 * @b: the second number
 * Return: always (a + b)
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - it subtracts two numbers
 * @a: first number
 * @b: the second number
 * Return: always (a - b)
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - it multiplies two numbers
 * @a: first number
 * @b: the second number
 * Return: always (a * b)
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - it devides two numbers
 * @a: first number
 * @b: the second number
 * Return: always (a / b)
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - it finds the modules of two numbers
 * @a: first number
 * @b: the second number
 * Return: always (a % b)
 */
int op_mod(int a, int b)
{
	return (a % b);
}
